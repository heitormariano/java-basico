package com.loiane.cursojava.aula29;

public class Bicicleta {

	String marca;
	String geneno;
	int aro;
	int garantiaMeses;

	public Bicicleta() {
	}

	public Bicicleta(String marca, String geneno, int aro, int garantiaMeses) {
		this.marca = marca;
		this.geneno = geneno;
		this.aro = aro;
		this.garantiaMeses = garantiaMeses;
	}
}
