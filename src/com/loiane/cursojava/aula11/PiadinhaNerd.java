package com.loiane.cursojava.aula11;

public class PiadinhaNerd {

	//31 de outubro (octal) é igual a 25 de dezembro(decimal)
	
	public static void main(String[] args) {
		int oct31 = 031; //octal
		int dec25 = 25; //Decimal
		
		if (oct31 == dec25) {
			System.out.println("Igual!");
		} else {
			System.out.println("Não são iguais!");
		}
	}
	

}
