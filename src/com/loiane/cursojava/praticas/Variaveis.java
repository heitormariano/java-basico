package com.loiane.cursojava.praticas;

public class Variaveis {

  public static void main(String[] args) {
;
    int numero = 100;
    float numFloat = 3.4f;
    String nomePessoa = "Fabiana";
    char letra = 'A';
    
    System.out.printf("O número é: %d%n", numero);
    System.out.printf("O número é: %.2f%n", numFloat);
    System.out.printf("O nome é: %s%n", nomePessoa);
    System.out.printf("A letra é: %s%n", letra);
    
  }
}
